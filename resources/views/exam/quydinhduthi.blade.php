        <h2>1. Thí sinh phải có mặt đúng giờ, đúng địa điểm</h2>
        <p><strong>•</strong> Thí sinh phải <strong class="highlight-blue-bold">có mặt tại phòng thi đúng thời gian và địa điểm</strong> ghi trong thông báo để làm thủ tục dự thi: <u>nhận thẻ dự thi, kiểm tra thông tin</u>, nộp phiếu đăng ký dự thi, nghe phổ biến quy chế, tham dự đầy đủ các môn thi.</p>
        <p><strong>•</strong> Nếu <strong class="highlight-red-bold">phát hiện sai sót</strong> trên thẻ dự thi, thí sinh báo ngay cho CBCT để xử lý.</p>
        <p><strong>•</strong> Nếu <strong class="highlight-red-bold">mất thẻ dự thi</strong>, phải đăng ký làm lại tại <strong>P410-T1</strong> để thi tiếp.</p>
        <p><strong>•</strong> Thí sinh đến <strong class="highlight-red-bold">chậm quá 15 phút</strong> sau hiệu lệnh làm bài <strong class="highlight-red-bold">không được dự thi</strong>.</p>
        <p><strong>•</strong> Vắng môn thi <u>ngày thứ nhất</u> sẽ <strong class="highlight-red-bold">không được thi tiếp</strong>.</p>

        <h2>2. Quy định về vật dụng mang vào phòng thi</h2>
        <p><strong>Thí sinh phải xuất trình:</strong></p>
        <ul>
            <li><strong class="highlight-blue-bold">Căn cước công dân, phiếu đăng ký dự thi</strong> vào buổi thi đầu tiên.</li>
            <li><strong class="highlight-blue-bold">Thẻ dự thi</strong> vào các buổi thi tiếp theo.</li>
        </ul>
        <p><strong>•</strong> Chỉ được mang vào: <u>bút viết, bút chì, compa, tẩy, thước kẻ, máy tính không soạn thảo</u>.</p>
        <p><strong>•</strong> <strong class="highlight-red-bold">Cấm mang</strong> điện thoại, đồng hồ thông minh, thiết bị ghi âm/hình, vật dụng cá nhân (ba lô, đồ ăn/uống, ô, mũ...) vào phòng thi. Thí sinh phải để những đồ dùng này <u>ngoài hành lang</u>.</p>
        <p><strong>•</strong> <strong class="highlight-red-bold">Cấm hút thuốc</strong> trong phòng thi.</p>

        <h2>3. Quy định làm bài thi</h2>
        <p><strong>•</strong> Ghi đầy đủ <strong>số báo danh</strong> vào giấy thi và giấy nháp. Kiểm tra đầy đủ <u>chữ ký và họ tên 2 CBCT</u> trước khi nộp.</p>
        <p><strong>•</strong> Bài làm phải <strong class="highlight-blue-bold">rõ ràng, sạch sẽ, không dùng mực đỏ hoặc bút chì</strong> (trừ hình tròn bằng compa).</p>
        <p><strong>•</strong> <strong class="highlight-red-bold">Cấm làm ký hiệu riêng, không được đánh dấu</strong>, không dùng bút xóa.</p>
        <p><strong>•</strong> <strong class="highlight-red-bold">Nghiêm cấm gian lận</strong>, không xem bài, không trao đổi khi làm bài.</p>
        <p><strong>•</strong> Mọi thắc mắc phải hỏi <u>công khai</u>. Nếu ốm đau bất thường, báo CBCT để xử lý.</p>

        <h2>4. Nộp bài và rời phòng thi</h2>
        <p><strong>•</strong> Khi <strong class="highlight-red-bold">hết giờ</strong> phải <strong class="highlight-blue-bold">ngừng làm bài</strong> và nộp bài theo hướng dẫn của CBCT.</p>
        <p><strong>•</strong> Phải ghi rõ <u>số tờ giấy thi</u> và <strong>ký tên</strong> khi nộp bài.</p>
        <p><strong>•</strong> Chỉ được ra khỏi phòng thi sau <strong class="highlight-red-bold">2/3 thời gian làm bài</strong>, và phải <u>nộp lại đề thi, giấy nháp</u>.</p>

        <h2>5. Xử lý vi phạm</h2>
        <p><strong>•</strong> Vi phạm quy chế sẽ bị <strong class="highlight-red-bold">xử lý kỷ luật</strong>: khiển trách, cảnh cáo, đình chỉ thi, hoặc <u>truy cứu trách nhiệm hình sự</u> tùy theo mức độ.</p>
        <p><strong>•</strong> <strong>CBCT phải công bố hình thức xử lý</strong>. Nếu thí sinh không ký biên bản, hai CBCT sẽ ký thay.</p>

